# =========================================================
# CREATE PROJECT STRUCTURE
# =========================================================

def create_project_structure(

    project_name
):

    # =====================================================
    # ROOT
    # =====================================================

    root = Path(

        "outputs"
    ) / project_name

    # =====================================================
    # CREATE MAIN
    # =====================================================

    root.mkdir(

        parents=True,

        exist_ok=True
    )

    # =====================================================
    # FOLDERS
    # =====================================================

    folders = [

        "website",

        "branding",

        "social",

        "docs",

        "exports",

        "business_plan"
    ]

    # =====================================================
    # CREATE FOLDERS
    # =====================================================

    for folder in folders:

        (

            root / folder

        ).mkdir(

            exist_ok=True
        )

    # =====================================================
    # RETURN
    # =====================================================

    return root
